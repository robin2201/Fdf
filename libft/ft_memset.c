#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*p;

	p = (char *)s;
	i = 0;
	if (n == 0)
		return (s);
	while (i < n)
	{
		p[i] = c;
		i++;
	}
	return ((void *)s);
}
